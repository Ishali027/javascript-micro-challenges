function whichIsBigger( num1, num2){
    if( num1 > num2){
        return 'The first number is bigger!';
    } else if ( num1 < num2){
        return 'The second number is bigger!';
    } else{
        return 'The numbers are the same';
    }
}
result = whichIsBigger(3,8);
